// print number 1 to 10
#include <iostream>
using namespace std;
int main()
{
    int i;

    while (i <= 10)
    {
        cout << i << endl;
        i++;
    }
}

// print number fromn to 1
#include <iostream>
using namespace std;
int main()
{
    int n;
    cin >> n;
    int i = n;
    while (i >= 1)
    {
        cout << i << endl;
        i--;
    }
}